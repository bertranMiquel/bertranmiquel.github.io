const year = document.getElementById("currentYear");
const themeToggle = document.getElementById("themeToggle");
const langToggle = document.getElementById("langToggle");
const topButton = document.getElementById("topBtn");
const brandLink = document.querySelector(".brand");
const navLinks = Array.from(document.querySelectorAll(".nav a"));
const metaDescription = document.getElementById("metaDescription");
const metaOgTitle = document.getElementById("metaOgTitle");
const metaOgDescription = document.getElementById("metaOgDescription");
const sections = navLinks
  .map((link) => document.querySelector(link.getAttribute("href")))
  .filter(Boolean);

if (year) {
  year.textContent = new Date().getFullYear();
}

const storedTheme = localStorage.getItem("theme");
const prefersLight = window.matchMedia("(prefers-color-scheme: light)").matches;

if (storedTheme === "light" || (!storedTheme && prefersLight)) {
  document.body.classList.add("light-theme");
}

function applyTranslations(language) {
  const locale = translations[language] ?? translations.en;

  document.documentElement.lang = locale.htmlLang;
  document.title = locale.pageTitle;

  if (metaDescription) metaDescription.setAttribute("content", locale.metaDescription);
  if (metaOgTitle) metaOgTitle.setAttribute("content", locale.metaOgTitle);
  if (metaOgDescription) metaOgDescription.setAttribute("content", locale.metaOgDescription);

  document.querySelectorAll("[data-i18n]").forEach((element) => {
    const key = element.getAttribute("data-i18n");
    if (key && locale[key]) {
      element.textContent = locale[key];
    }
  });

  document.querySelectorAll("[data-i18n-attr]").forEach((element) => {
    const mappings = element.getAttribute("data-i18n-attr");
    if (!mappings) return;

    mappings.split(",").forEach((mapping) => {
      const [attribute, key] = mapping.split(":").map((value) => value.trim());
      if (attribute && key && locale[key]) {
        element.setAttribute(attribute, locale[key]);
      }
    });
  });

  langToggle?.setAttribute("data-language", language);
  langToggle?.setAttribute("aria-label", locale.langToggle);

  updateThemeLabel(language);
  topButton?.setAttribute("aria-label", locale.topAria);
}

function updateThemeLabel(language = currentLanguage) {
  const locale = translations[language] ?? translations.en;
  const isLight = document.body.classList.contains("light-theme");
  themeToggle?.setAttribute("aria-label", isLight ? locale.themeToggleDark : locale.themeToggleLight);
}

let currentLanguage = localStorage.getItem("language");
if (!translations[currentLanguage]) {
  currentLanguage = "en";
}

applyTranslations(currentLanguage);

themeToggle?.addEventListener("click", () => {
  document.body.classList.toggle("light-theme");
  localStorage.setItem("theme", document.body.classList.contains("light-theme") ? "light" : "dark");
  updateThemeLabel();
});

langToggle?.addEventListener("click", () => {
  currentLanguage = currentLanguage === "en" ? "ca" : "en";
  localStorage.setItem("language", currentLanguage);
  applyTranslations(currentLanguage);
});

topButton?.addEventListener("click", () => {
  window.scrollTo({ top: 0, behavior: "smooth" });
});

brandLink?.addEventListener("click", (event) => {
  event.preventDefault();
  window.scrollTo({ top: 0, behavior: "smooth" });
});

const revealObserver = new IntersectionObserver(
  (entries) => {
    entries.forEach((entry) => {
      if (entry.isIntersecting) {
        entry.target.classList.add("visible");
        revealObserver.unobserve(entry.target);
      }
    });
  },
  { threshold: 0.14 }
);

document.querySelectorAll(".reveal").forEach((element) => {
  revealObserver.observe(element);
});

const navObserver = new IntersectionObserver(
  (entries) => {
    entries.forEach((entry) => {
      if (!entry.isIntersecting) return;

      navLinks.forEach((link) => {
        link.classList.toggle("active", link.getAttribute("href") === `#${entry.target.id}`);
      });
    });
  },
  { rootMargin: "-35% 0px -55% 0px", threshold: 0.01 }
);

sections.forEach((section) => navObserver.observe(section));

function updateTopButton() {
  topButton?.classList.toggle("visible", window.scrollY > 700);
}

window.addEventListener("scroll", updateTopButton, { passive: true });
updateTopButton();
